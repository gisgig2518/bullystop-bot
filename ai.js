const axios = require('axios');

const SYSTEM_PROMPT = `คุณคือ BullyStop Bot เพื่อนที่คอยช่วยเหลือในบอร์ดเกม STAND UP!
ซึ่งเป็นเกมเสริมทักษะชีวิตในการรับมือกับการบูลลี่ สำหรับนักเรียนชั้นมัธยมศึกษาตอนต้น

สไตล์การพูด:
- พูดเป็นกันเองเหมือนเพื่อน ใช้คำว่า "เธอ" และ "ฉัน" แทน "คุณ" และ "ผม/หนู"
- ใช้ภาษาง่ายๆ สั้นๆ เข้าใจได้ทันที
- ใส่ emoji เล็กน้อยเพื่อให้ดูเป็นมิตร เช่น 😊 💙 💪 🎮
- ตอบไม่เกิน 3-4 ประโยค
- อบอุ่น ให้กำลังใจ และไม่ตัดสิน

หน้าที่:
1. ให้คำแนะนำเรื่องการรับมือกับการบูลลี่
2. อธิบายทักษะชีวิต เช่น การสื่อสาร Upstander การขอความช่วยเหลือ
3. ตอบคำถามเกี่ยวกับกติกาเกม
4. ให้กำลังใจและสนับสนุน

ข้อห้าม:
- ถ้าดูเหมือนเจอปัญหาจริง ให้แนะนำติดต่อครูหรือสายด่วน 1387 ทุกครั้ง
- ไม่ตอบเรื่องที่ไม่เกี่ยวกับเกมหรือการบูลลี่ ให้โอนกลับมาที่หัวข้อนี้`;

async function ask(userMessage, state = {}) {
  if (!process.env.CLAUDE_API_KEY) {
    return fallback(userMessage);
  }
  try {
    const res = await axios.post(
      'https://api.anthropic.com/v1/messages',
      {
        model: 'claude-sonnet-4-20250514',
        max_tokens: 250,
        system: SYSTEM_PROMPT,
        messages: [{ role: 'user', content: userMessage }],
      },
      {
        headers: {
          'x-api-key': process.env.CLAUDE_API_KEY,
          'anthropic-version': '2023-06-01',
          'content-type': 'application/json',
        },
        timeout: 10000,
      }
    );
    return res.data.content[0]?.text || fallback(userMessage);
  } catch (err) {
    console.error('AI error:', err.message);
    return fallback(userMessage);
  }
}

// Rule-based สำหรับเมื่อไม่มี API Key
function fallback(text) {
  const t = text.toLowerCase();

  if (t.includes('upstander') || t.includes('ช่วยเพื่อน') || t.includes('หยุดบูลลี่')) {
    return 'Upstander คือคนที่เห็นการบูลลี่แล้วกล้าพูดหรือทำอะไรบางอย่างเพื่อหยุดยั้ง แทนที่จะนิ่งเฉยครับ แค่บอกว่า "หยุดได้แล้ว" หรือยืนอยู่ข้างเพื่อนก็ช่วยได้มากแล้วนะ 💙';
  }
  if (t.includes('บูลลี่') || t.includes('ถูกรังแก') || t.includes('ล้อเลียน')) {
    return 'เข้าใจเลยครับ สิ่งแรกที่ทำได้คือบอกตรงๆ ว่าไม่โอเคกับสิ่งที่เกิดขึ้น ถ้าไม่ดีขึ้นให้แจ้งครูหรือผู้ปกครองทันทีนะครับ เธอไม่ได้อยู่คนเดียว 💪';
  }
  if (t.includes('กลัว') || t.includes('เครียด') || t.includes('เจ็บปวด') || t.includes('เศร้า')) {
    return 'ฉันเข้าใจนะครับ ความรู้สึกแบบนี้มันหนักมากเลย อย่าแบกไว้คนเดียวนะ ลองคุยกับครูแนะแนวหรือโทรสายด่วน 1387 ได้เลยครับ 💙';
  }
  if (t.includes('สแกน') || t.includes('qr') || t.includes('เชื่อมต่อ') || t.includes('เข้าไม่ได้')) {
    return 'สแกน QR Code กลางกระดาน STAND UP! ด้วยแอป LINE แล้วกด "เพิ่มเพื่อน" ได้เลยนะครับ ถ้ายังไม่ได้ลองปิดแอปแล้วเปิดใหม่ครับ 😊';
  }
  if (t.includes('คะแนน') || t.includes('ชนะ') || t.includes('ได้เท่าไหร่')) {
    return 'ผู้เล่นที่เดินครบ 40 ช่องก่อนคือผู้ชนะครับ แต่คะแนนทักษะจากการตอบสถานการณ์บูลลี่ถูกต้องสำคัญกว่านะ เพราะนั่นคือสิ่งที่เอาไปใช้ในชีวิตจริงได้เลย 💪';
  }
  if (t.includes('ช่องบูลลี่') || t.includes('ช่องโอกาส') || t.includes('ช่องทักษะ')) {
    return 'ช่องสีส้ม = บูลลี่ รับสถานการณ์จากฉัน, ช่องสีม่วง = ทักษะ หยิบการ์ดความรู้, ช่องสีเหลือง = โอกาส สุ่มเหตุการณ์ครับ 🎲';
  }

  return 'ขอโทษนะครับ ฉันยังไม่เข้าใจสิ่งที่เธอพิมพ์มาเลย 😅\n\nลองพิมพ์ได้เลยครับ:\n🎮 "เริ่มเล่น" — เริ่มทำ Pre-test\n🃏 "การ์ด" — รับสถานการณ์ใหม่\n📊 "คะแนน" — ดูคะแนนสะสม\n🆘 "ขอความช่วยเหลือ" — ติดต่อขอความช่วยเหลือ';
}

module.exports = { ask };
