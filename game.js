function getRandomCard() { return BULLY_CARDS[Math.floor(Math.random()*BULLY_CARDS.length)]; }
function getCardById(id) { return BULLY_CARDS.find(c=>c.id===id)||getRandomCard(); }
function checkAnswer(cardId, choiceKey) {
  const card=getCardById(cardId), choice=card.choices.find(c=>c.key===choiceKey);
  const score=choice?choice.score:0, best=card.choices.reduce((a,b)=>a.score>b.score?a:b);
  return {card,selected:choice,score,isCorrect:choice&&choice.key===best.key,bestChoice:best,explain:card.explain};
}
function getPretestQuestion(index) { return TEST_QUESTIONS[index%TEST_QUESTIONS.length]; }

// ตอบถูก = 1 คะแนน / ตอบผิด = 0 คะแนน / เต็ม 10
function scorePre(answers) {
  return answers.reduce((total, a) => {
    const q = TEST_QUESTIONS.find(q => q.id === a.q);
    return total + (q && a.a === q.correctKey ? 1 : 0);
  }, 0);
}

